const buildString = ({ loneCodePoints, ranges }) => {
	const CHUNK_SIZE = 10000;
	let result = String.fromCodePoint(...loneCodePoints);
	for (const [start, end] of ranges) {
		const codePoints = [];
		for (let length = 0, codePoint = start; codePoint <= end; codePoint++) {
			codePoints[length++] = codePoint;
			if (length === CHUNK_SIZE) {
				result += String.fromCodePoint(...codePoints);
				codePoints.length = length = 0;
			}
		}
		result += String.fromCodePoint(...codePoints);
	}
	return result;
};

const matchSymbols = buildString({
	loneCodePoints: [],
	ranges: [
		[0x000030, 0x000039],
		[0x000041, 0x000046],
		[0x000061, 0x000066]
	]
});
assert(
	/^\p{ASCII_Hex_Digit}+$/u.test(matchSymbols),
	"`\\p{ASCII_Hex_Digit}` matches all proper symbols"
);
assert(
	/^\p{AHex}+$/u.test(matchSymbols),
	"`\\p{AHex}` matches all proper symbols"
);

const nonMatchSymbols = buildString({
	loneCodePoints: [],
	ranges: [
		[0x00DC00, 0x00DFFF],
		[0x000000, 0x00002F],
		[0x00003A, 0x000040],
		[0x000047, 0x000060],
		[0x000067, 0x00DBFF],
		[0x00E000, 0x10FFFF]
	]
});
assert(
	/^\P{ASCII_Hex_Digit}+$/u.test(nonMatchSymbols),
	"`\\P{ASCII_Hex_Digit}` matches all proper symbols"
);
assert(
	/^\P{AHex}+$/u.test(nonMatchSymbols),
	"`\\P{AHex}` matches all proper symbols"
);
