const matchSymbols = buildString({
  loneCodePoints: [],
  ranges: [
    [0x01F3FB, 0x01F3FF]
  ]
});
assert(
  /^\p{Emoji_Modifier}+$/u.test(matchSymbols),
  "`\\p{Emoji_Modifier}` matches all proper symbols"
);

const nonMatchSymbols = buildString({
  loneCodePoints: [],
  ranges: [
    [0x00DC00, 0x00DFFF],
    [0x000000, 0x00DBFF],
    [0x00E000, 0x01F3FA],
    [0x01F400, 0x10FFFF]
  ]
});
assert(
  /^\P{Emoji_Modifier}+$/u.test(nonMatchSymbols),
  "`\\P{Emoji_Modifier}` matches all proper symbols"
);
