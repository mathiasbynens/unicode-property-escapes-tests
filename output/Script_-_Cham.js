const buildString = ({ loneCodePoints, ranges }) => {
	const CHUNK_SIZE = 10000;
	let result = String.fromCodePoint(...loneCodePoints);
	for (const [start, end] of ranges) {
		const codePoints = [];
		for (let length = 0, codePoint = start; codePoint <= end; codePoint++) {
			codePoints[length++] = codePoint;
			if (length === CHUNK_SIZE) {
				result += String.fromCodePoint(...codePoints);
				codePoints.length = length = 0;
			}
		}
		result += String.fromCodePoint(...codePoints);
	}
	return result;
};

const matchSymbols = buildString({
	loneCodePoints: [],
	ranges: [
		[0x00AA00, 0x00AA36],
		[0x00AA40, 0x00AA4D],
		[0x00AA50, 0x00AA59],
		[0x00AA5C, 0x00AA5F]
	]
});
assert(
	/^\p{Script=Cham}+$/u.test(matchSymbols),
	"`\\p{Script=Cham}` matches all proper symbols"
);
assert(
	/^\p{Script=Cham}+$/u.test(matchSymbols),
	"`\\p{Script=Cham}` matches all proper symbols"
);
assert(
	/^\p{sc=Cham}+$/u.test(matchSymbols),
	"`\\p{sc=Cham}` matches all proper symbols"
);
assert(
	/^\p{sc=Cham}+$/u.test(matchSymbols),
	"`\\p{sc=Cham}` matches all proper symbols"
);

const nonMatchSymbols = buildString({
	loneCodePoints: [],
	ranges: [
		[0x00DC00, 0x00DFFF],
		[0x000000, 0x00A9FF],
		[0x00AA37, 0x00AA3F],
		[0x00AA4E, 0x00AA4F],
		[0x00AA5A, 0x00AA5B],
		[0x00AA60, 0x00DBFF],
		[0x00E000, 0x10FFFF]
	]
});
assert(
	/^\P{Script=Cham}+$/u.test(nonMatchSymbols),
	"`\\P{Script=Cham}` matches all proper symbols"
);
assert(
	/^\P{Script=Cham}+$/u.test(nonMatchSymbols),
	"`\\P{Script=Cham}` matches all proper symbols"
);
assert(
	/^\P{sc=Cham}+$/u.test(nonMatchSymbols),
	"`\\P{sc=Cham}` matches all proper symbols"
);
assert(
	/^\P{sc=Cham}+$/u.test(nonMatchSymbols),
	"`\\P{sc=Cham}` matches all proper symbols"
);
