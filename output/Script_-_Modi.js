const matchSymbols = buildString({
  loneCodePoints: [],
  ranges: [
    [0x011600, 0x011644],
    [0x011650, 0x011659]
  ]
});
assert(
  /^\p{Script=Modi}+$/u.test(matchSymbols),
  "`\\p{Script=Modi}` matches all proper symbols"
);
assert(
  /^\p{Script=Modi}+$/u.test(matchSymbols),
  "`\\p{Script=Modi}` matches all proper symbols"
);
assert(
  /^\p{sc=Modi}+$/u.test(matchSymbols),
  "`\\p{sc=Modi}` matches all proper symbols"
);
assert(
  /^\p{sc=Modi}+$/u.test(matchSymbols),
  "`\\p{sc=Modi}` matches all proper symbols"
);

const nonMatchSymbols = buildString({
  loneCodePoints: [],
  ranges: [
    [0x00DC00, 0x00DFFF],
    [0x000000, 0x00DBFF],
    [0x00E000, 0x0115FF],
    [0x011645, 0x01164F],
    [0x01165A, 0x10FFFF]
  ]
});
assert(
  /^\P{Script=Modi}+$/u.test(nonMatchSymbols),
  "`\\P{Script=Modi}` matches all proper symbols"
);
assert(
  /^\P{Script=Modi}+$/u.test(nonMatchSymbols),
  "`\\P{Script=Modi}` matches all proper symbols"
);
assert(
  /^\P{sc=Modi}+$/u.test(nonMatchSymbols),
  "`\\P{sc=Modi}` matches all proper symbols"
);
assert(
  /^\P{sc=Modi}+$/u.test(nonMatchSymbols),
  "`\\P{sc=Modi}` matches all proper symbols"
);
